public class StarPattern{
    public static void main(String[] args){
        // using \n
        System.out.print("*\n**\n***\n****\n*****\n");
        // using println
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("*****");
    }
}